<p>Le diagnostic environnemental est réalisé au travers d’un questionnaire semi-structuré. Celui-ci est divisé en 4 thématiques :<p>

<ol>
    <li>Les effets de l’environnement sur l’activité du producteur ou de la productrice ;</li>
    <li>Les effets de l’activité du producteur ou de la productrice sur l’environnement ;</li>
    <li>La volonté d’engagement du producteur ou de la productrice pour l’environnement ;</li>
    <li>Ses capacités et ses besoins pour s’engager.</li>
</ol>

<p>L’idée est de mettre en évidence les connaissances et perceptions du producteur ou de la productrice par rapport à l’environnement, et de dégager les principales relations réciproques (positives ou négatives) entre l’activité et l’environnement.</p>

<p>Cette étape de diagnostic environnemental comporte :<br/>
<ol>
    <li>Les modalités d’application : identification du producteur ou de la productrice, description des possibles tiers présents durant le diagnostic et leur rôle (d’observateur, de facilitateur, etc.), et détermination de qui attribue les scores des indicateurs ;</li>

    <li>Une description brève de l’activité du producteur ou de la productrice ;</li>
    <li>Une description brève du contexte environnemental, qui offre un référentiel de la situation environnementale dans la zone ou le secteur d’activité ;</li>
    <li>Une description brève des effets réciproques entre l’environnement et l’activité du producteur ou de la productrice ;</li>
    <li>Un questionnaire semi-structuré par thématique ;</li>
    <li>Une synthèse qui reprend l’ensemble des scores, ainsi que les principales actions identifiées pour améliorer la situation environnementale de l’activité ;</li>
    <li>Les leçons apprises permettant de mettre en évidence ce que les protagonistes retirent du diagnostic, et d’éventuelles propositions d’amélioration.</li>
</ol>
</p>

<p>Chaque questionnaire par thématique se compose de :
<ul>Questions cibles : elles ne doivent pas forcément suivre l’ordre établi et elles peuvent être adaptées (voire supprimées) selon le contexte ;
    <li>Un indicateur de pondération permettant d’évaluer chaque thématique. Il ne s’agit pas d’un contrôle formel, mais d’un bilan informatif des connaissances et capacités du producteur ou de la productrice. Pour un diagnostic optimal, il est important que le producteur ou la productrice reste objectif et critique par rapport à son activité ;</li>
    <li>Un tableau reprenant la synthèse des réponses aux questions, le score attribué pour la thématique, ainsi que des commentaires éventuels.</li>
</ul>
